    static func string(_ key: Key) -> String {
        string(key.rawValue)
    }

    static func string(_ key: String, default defaultValue: String? = nil) -> String {
        let fallback = defaultValue ?? key
        if let nativeLangLocalized = currentNativeLangTranslations?.localizedValue(for: key) {
            return nativeLangLocalized
        }
        if let nativeLangLocalized = currentNativeLangTranslations?.localizedValue(forDefaultValue: fallback) {
            return nativeLangLocalized
        }

        if let table = stringsTable(for: currentLocalizationFileName),
           let localized = table[key] {
            return localized.isEmpty ? fallback : localized
        }
        return fallback
    }

    static func messageBox(tag: String, defaultTitle: String, defaultMessage: String) -> (title: String, message: String) {
        if let localizedMessage = currentNativeLangTranslations?.messageBox(tag: tag) {
            return (localizedMessage.title, localizedMessage.message)
        }

        return (defaultTitle, defaultMessage)
    }

    static let localizationDidChangeNotification = Notification.Name("NotepadMacLocalizationDidChange")

    nonisolated(unsafe) private(set) static var currentLocalizationFileName = AppPreferences.defaultValue.localizationFileName
    nonisolated(unsafe) private static var currentNativeLangTranslations: NativeLangTranslations?

    static func apply(localizationFileName: String, postNotification: Bool = true) {
        let normalizedFileName = localizationFileName.isEmpty
            ? AppPreferences.defaultValue.localizationFileName
            : localizationFileName

        guard currentLocalizationFileName != normalizedFileName else { return }
        currentLocalizationFileName = normalizedFileName
        cachedStringsTable = nil
        currentNativeLangTranslations = bundleLocalizationIsBuiltIn(normalizedFileName)
            ? nil
            : NativeLangTranslations.load(fileName: normalizedFileName, bundle: resourceBundle)

        if postNotification {
            NotificationCenter.default.post(name: localizationDidChangeNotification, object: nil)
        }
    }

    nonisolated(unsafe) private static var cachedStringsTable: (fileName: String, table: [String: String])?

    private static func stringsTable(for localizationFileName: String) -> [String: String]? {
        if let cached = cachedStringsTable, cached.fileName == localizationFileName {
            return cached.table
        }
        let localeIdentifier: String
        switch localizationFileName.lowercased() {
        case "english.xml", "english_customizable.xml":
            localeIdentifier = "en"
        case "chinesesimplified.xml":
            localeIdentifier = "zh-hans"
        default:
            return nil
        }
        let stringsURL = resourceBundle.bundleURL
            .appending(path: "\(localeIdentifier).lproj")
            .appending(path: "Localizable.strings")
        guard let table = NSDictionary(contentsOfFile: stringsURL.path) as? [String: String] else {
            return nil
        }
        cachedStringsTable = (fileName: localizationFileName, table: table)
        return table
    }

    static func bundle(for localizationFileName: String) -> Bundle {
        let localeIdentifier = switch localizationFileName.lowercased() {
        case "english.xml", "english_customizable.xml":
            "en"
        case "chinesesimplified.xml":
            "zh-hans"
        default:
            "en"
        }

        let lprojURL = resourceBundle.bundleURL.appending(path: "\(localeIdentifier).lproj")
        guard FileManager.default.fileExists(atPath: lprojURL.path),
              let bundle = Bundle(url: lprojURL)
        else {
            return resourceBundle
        }

        return bundle
    }

    private static func bundleLocalizationIsBuiltIn(_ localizationFileName: String) -> Bool {
        switch localizationFileName.lowercased() {
        case "english.xml", "english_customizable.xml", "chinesesimplified.xml":
            true
        default:
            false
        }
    }

    static let resourceBundle: Bundle = {
        let bundleName = "NotepadMac_NotepadMac.bundle"
        let packagedBundle = Bundle.main.resourceURL?.appending(path: bundleName)
        let adjacentBundle = Bundle.main.bundleURL.appending(path: bundleName)

        for url in [packagedBundle, adjacentBundle].compactMap(\.self) {
            if let bundle = Bundle(url: url) {
                return bundle
            }
        }

        return .module
    }()
