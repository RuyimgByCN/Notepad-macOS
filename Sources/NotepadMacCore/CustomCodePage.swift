public extension String.Encoding {
    /// Synthetic encodings for the custom OEM code pages. The raw values
    /// are far outside the CFStringEncoding-derived range so they cannot
    /// collide with real Foundation encodings.
    static let dosCP720Encoding = String.Encoding(rawValue: 0x4E50_0720)
    static let dosCP858Encoding = String.Encoding(rawValue: 0x4E50_0858)
    static let dosCP861Encoding = String.Encoding(rawValue: 0x4E50_0861)
}
