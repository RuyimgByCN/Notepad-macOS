import Foundation

/// Byte-table codecs for the OEM code pages macOS CoreFoundation cannot
/// decode correctly: OEM 720 (Arabic) and OEM 858 (Western European with
/// the euro sign) have no CF encoding at all, and CF's table for OEM 861
/// (Icelandic, kCFStringEncodingDOSIcelandic) actually produces cp775
/// mappings. Tables follow the Unicode Consortium / IBM reference
/// mappings; bytes 0x00–0x7F are ASCII identity for all three pages.
public enum CustomCodePage: CaseIterable, Sendable {
    case oem720
    case oem858
    case oem861

    /// Synthetic `String.Encoding` raw values used to thread these pages
    /// through APIs that carry a `String.Encoding`. They must never be
    /// passed to Foundation conversion APIs directly; `TextFileCodec`
    /// routes them here.
    public var encoding: String.Encoding {
        switch self {
        case .oem720: .dosCP720Encoding
        case .oem858: .dosCP858Encoding
        case .oem861: .dosCP861Encoding
        }
    }

    public init?(encoding: String.Encoding) {
        switch encoding {
        case .dosCP720Encoding: self = .oem720
        case .dosCP858Encoding: self = .oem858
        case .dosCP861Encoding: self = .oem861
        default: return nil
        }
    }

    /// Unicode scalar for each byte 0x80...0xFF.
    var highTable: [UInt32] {
        switch self {
        case .oem720: Self.cp720High
        case .oem858: Self.cp858High
        case .oem861: Self.cp861High
        }
    }

    public func decode(_ data: Data) -> String? {
        let table = highTable
        var view = String.UnicodeScalarView()
        view.reserveCapacity(data.count)
        for byte in data {
            let value: UInt32 = byte < 0x80 ? UInt32(byte) : table[Int(byte) - 0x80]
            guard let scalar = UnicodeScalar(value) else { return nil }
            view.append(scalar)
        }
        return String(view)
    }

    public func encode(_ string: String) -> Data? {
        let reverse = reverseTable
        var data = Data()
        data.reserveCapacity(string.unicodeScalars.count)
        for scalar in string.unicodeScalars {
            if scalar.value < 0x80 {
                data.append(UInt8(scalar.value))
            } else if let byte = reverse[scalar.value] {
                data.append(byte)
            } else {
                return nil
            }
        }
        return data
    }

    private var reverseTable: [UInt32: UInt8] {
        switch self {
        case .oem720: Self.cp720Reverse
        case .oem858: Self.cp858Reverse
        case .oem861: Self.cp861Reverse
        }
    }

    private static func reversed(_ table: [UInt32]) -> [UInt32: UInt8] {
        var map: [UInt32: UInt8] = [:]
        map.reserveCapacity(table.count)
        for (offset, scalar) in table.enumerated() {
            // Keep the first byte when a scalar appears twice.
            if map[scalar] == nil {
                map[scalar] = UInt8(offset + 0x80)
            }
        }
        return map
    }

